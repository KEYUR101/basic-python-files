num1 = float(input("enter a numer: "))
op = input("enter operator: ")
num2 = float(input("enter another numer: "))
if op == "+":
    print(num1 + num2)
elif op =="-":
    print(num1 - num2)
elif op == "/":
    print(num1 / num2)
elif op == "*":
    print(num1 * num2)
elif op == "%":
    print(num1 % num2)
else:
    print("invalid operation")


        





